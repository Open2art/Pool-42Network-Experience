#include <unistd.h>

void ft_ultimate_div_mod(int *a, int *b)
{
	int div;
	int res;
	
	if (*b != 0)
	{
		div = *a/(*b);
		res = *a%(*b);
		*a = div;
		*b = res;
	}
}
